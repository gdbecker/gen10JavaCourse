class Cafeteria {
    private int numStoves;
    private int seatingCapacity;
    private double squareFootage;
    private int numServingLines;
    private int mealsPerHour;

    public int getNumStoves() {
        return numStoves;
    }

    public void setNumStoves(int numStoves) {
        this.numStoves = numStoves;
    }

    public int getSeatingCapacity() {
        return seatingCapacity;
    }

    public void setSeatingCapacity(int seatingCapacity) {
        this.seatingCapacity = seatingCapacity;
    }

    public double getSquareFootage() {
        return squareFootage;
    }

    public void setSquareFootage(double squareFootage) {
        this.squareFootage = squareFootage;
    }

    public int getNumServingLines() {
        return numServingLines;
    }

    public void setNumServingLines(int numServingLines) {
        this.numServingLines = numServingLines;
    }

    public int getMealsPerHour() {
        return mealsPerHour;
    }

    public void setMealsPerHour(int mealsPerHour) {
        this.mealsPerHour = mealsPerHour;
    }
}
